<h3 id="_3-1getting-started" tabindex="-1"><a class="header-anchor" href="#_3-1getting-started" aria-hidden="true">#</a> 3.1	Getting started</h3>
<p><strong>START Docker:</strong></p>
<ul>
<li>Linux: Docker will start automatically after the computer starts</li>
<li>Docker for Windows: start Docker by clicking on the Docker icon (in ‘Programs’) – wait until “Docker is running” pops up</li>
<li>Docker Toolbox: open the Docker QuickStart terminal and wait until “Docker is configured to use default machine…” pops up</li>
</ul>
<p><strong>START OCR4all:</strong></p>
<ul>
<li>Linux: open the terminal, type in <code>docker start -ia ocr4all</code>, press 'enter' and wait for the server to start</li>
<li>Windows 10 (Home, Pro, Enterprise, Education): open Windows PowerShell, type in <code>docker start -ia ocr4all</code>, press 'enter' and wait for the server to start</li>
<li>Older Versions of Windows (with Docker Toolbox): open the Docker QuickStart terminal and wait until “Docker is configured to use default machine…” pops up</li>
</ul>
<p>After this initial installation, you will be able to easily access OCR4all in your browser, respectively under:</p>
<ul>
<li>Linux, Docker for Windows, MacOS: http://localhost:1476/ocr4all/</li>
<li>Docker Toolbox: http://192.168.99.100:1476/ocr4all/</li>
</ul>
